class Game:
    """Define the class game."""

    def __init__(self, size):
        """Define init."""
        self.board = np.zeros((size, size))

    def play(self):
        """Define play."""
        print("Playing life. Press ctrl + c to stop.")
        pyplot.ion()
        while True:
            self.move()
            self.show()
            pyplot.pause(0.0000005)

    def move(self):
        """Define move."""
        stencil = np.array([[1, 1, 1], [1, 0, 1], [1, 1, 1]])
        neighbourcount = convolve2d(self.board, stencil, mode='same')

        for i in range(self.board.shape[0]):
            for j in range(self.board.shape[1]):
                self.board[i, j] = 1 if (neighbourcount[i, j] == 3
                                         or (neighbourcount[i, j] == 2
                                             and self.board[i, j])) else 0

    def __setitem__(self, key, value):
        """Define setitem."""
        self.board[key] = value

    def show(self):
        """Define show."""
        pyplot.clf()
        pyplot.matshow(self.board, fignum=0, cmap='binary')
        pyplot.show()

    def insert(self, pattern, location):
        """Define insert."""
        x = location[0]
        y = location[1]
        self[(x - 1):(x + 2), (y - 1): (y + 2)] = pattern.grid


class Pattern:
    """Class pattern."""

    def __init__(self, grid):
        """Define init."""
        self.grid = grid

    def flip_vertical(self):
        """Define flip_vertical."""
        grid = self.grid[::-1]
        return Pattern(grid)

    def flip_horizontal(self):
        """Define flip_horizontal."""
        grid = self.grid.T[::-1].T
        return Pattern(grid)

    def flip_diag(self):
        """Define flip_diag."""
        grid = self.grid.T
        return Pattern(grid)

    def rotate(self, other):
        """Define rotate."""
        if isinstance(self, Pattern) and isinstance(other, Integral):
            a = other % 4
            if a == 0:
                return self
            elif a == 1:
                grid = self.grid.T[::-1]
                return Pattern(grid)
            elif a == 2:
                grid = self.grid[::-1].T[::-1].T
                return Pattern(grid)
            else:
                grid = self.grid[::-1].T
                return Pattern(grid)
        else:
            return NotImplemented
